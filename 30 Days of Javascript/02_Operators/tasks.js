// Comparison Operators
let e = 10;
let f = 20;

// Task 8
console.log(e < f);
console.log(e > f);

// Task 9
e = 20;
f = 20;
console.log(e <= f);
console.log(e >= f);

// Task 10
e = 10;
f = "10";
console.log(e == f);
console.log(e === f); //  Check for both the value and the data type

// Logical Operators
let bool1 = true;
let bool2 = false;

// Task 11
console.log(bool1 && bool2);
console.log(bool1 || bool2);
console.log(!bool1);
console.log(!bool2);

// Ternary Operator
let num = 2;
console.log(num > 0 ? "Number is positive" : "Number is negative");

num = -10;
console.log(num > 0 ? "Number is positive" : "Number is negative");

